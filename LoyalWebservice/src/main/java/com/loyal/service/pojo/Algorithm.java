package com.loyal.service.pojo;

public class Algorithm {
	
	private Integer algorithmID;
	
	private String algorithmDescription;
	
	private String algorithmMappingTable;

	public Integer getAlgorithmID() {
		return algorithmID;
	}

	public void setAlgorithmID(Integer algorithmID) {
		this.algorithmID = algorithmID;
	}

	public String getAlgorithmDescription() {
		return algorithmDescription;
	}

	public void setAlgorithmDescription(String algorithmDescription) {
		this.algorithmDescription = algorithmDescription;
	}

	public String getAlgorithmMappingTable() {
		return algorithmMappingTable;
	}

	public void setAlgorithmMappingTable(String algorithmMappingTable) {
		this.algorithmMappingTable = algorithmMappingTable;
	}

}
